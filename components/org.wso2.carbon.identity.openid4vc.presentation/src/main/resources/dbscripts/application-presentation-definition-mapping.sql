-- =====================================================================
-- Application Presentation Definition Mapping Schema
-- OpenID4VP per-application presentation definition configuration
-- 
-- This script creates the mapping table for associating presentation
-- definitions with specific applications.
-- =====================================================================

-- =====================================================================
-- MySQL/H2 Version
-- =====================================================================
CREATE TABLE IF NOT EXISTS IDN_APPLICATION_PRESENTATION_DEFINITION (
    ID INTEGER AUTO_INCREMENT,
    APPLICATION_ID VARCHAR(255) NOT NULL,
    PRESENTATION_DEFINITION_ID VARCHAR(255) NOT NULL,
    TENANT_ID INTEGER DEFAULT -1234,
    CREATED_AT BIGINT NOT NULL,
    UPDATED_AT BIGINT,
    PRIMARY KEY (ID),
    UNIQUE (APPLICATION_ID, TENANT_ID),
    FOREIGN KEY (PRESENTATION_DEFINITION_ID, TENANT_ID) REFERENCES IDN_PRESENTATION_DEFINITION(DEFINITION_ID, TENANT_ID)
);

-- Indexes for efficient lookups
CREATE INDEX IDX_APP_PRES_DEF_APP_ID ON IDN_APPLICATION_PRESENTATION_DEFINITION(APPLICATION_ID, TENANT_ID);
CREATE INDEX IDX_APP_PRES_DEF_PRES_ID ON IDN_APPLICATION_PRESENTATION_DEFINITION(PRESENTATION_DEFINITION_ID, TENANT_ID);
CREATE INDEX IDX_APP_PRES_DEF_TENANT ON IDN_APPLICATION_PRESENTATION_DEFINITION(TENANT_ID);


